void Wave_Output_Init(void)
{

    float f = 1000; //对应输出频率，单位Hz
    uint16_t arr;       //对应重置装载

    MX_GPIO_Init();
    MX_DMA_Init();
    MX_DAC_Init();
    MX_TIM6_Init();
    
	HAL_TIM_Base_Start(&htim6);
    HAL_DAC_Start_DMA(&hdac,DAC_CHANNEL_1,(uint32_t *)Sine12bit,N_WaveData,DAC_ALIGN_12B_R);
    // Tout=((arr+1)*(psc+1))/Ft us. Ft=定时器工作频率,单位:Mhz
    // psc=0 ; Ft=84 ; Tout/10^6=1/(N_WaveData*f)
    arr = (uint16_t)(1000000 * 84 / (N_WaveData * f) - 1); //已验证正确
    TIM6->ARR = arr;
    //HAL_DAC_Stop(&hdac,DAC_CHANNEL_1);
}
/**
 * @brief 暂时使用
 * 
 * @param f 输出频率，单位Hz
 */
void Wave_Output_F(float f)
{
    TIM6->ARR = (uint16_t)(1000000 * 84 / (N_WaveData * f) - 1);
}

